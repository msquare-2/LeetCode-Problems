#include<iostream>
using namespace std;

int main(){
    int a; //declaration
    a=10; //initailisation

    cout<<"sixe of int "<<sizeof(a)<<endl;

    float b;//float variable declaration
    cout<<"sixe of float "<<sizeof(b)<<endl;

    char c;//character variable declaration
    cout<<"sixe of char "<<sizeof(c)<<endl;

    bool d;//boolean variable declaration
    cout<<"sixe of bool "<<sizeof(d)<<endl;

    short int si;//typemodifier short int variable declaration
    cout<<"sixe of short int "<<sizeof(si)<<endl;

    long int li;//typemodifier long int variable declaration
    cout<<"sixe of long int "<<sizeof(li)<<endl;

}
